import fs from 'node:fs/promises';
import path from 'node:path';

const repoRoot = path.resolve(process.cwd());
const docsDir = path.join(repoRoot, 'docs-content');
const outDir = path.join(repoRoot, 'src', 'resources', 'pages');
const manifestPath = path.join(repoRoot, 'src', 'resources', 'pageResourcesManifest.ts');

function normalizeLineEndings(text) {
  return text.replace(/\r\n/g, '\n').replace(/\r/g, '\n');
}

function parseFrontmatter(raw) {
  const normalized = normalizeLineEndings(raw);
  if (!normalized.startsWith('---\n')) {
    return { data: {}, body: normalized };
  }

  const endIndex = normalized.indexOf('\n---\n', 4);
  if (endIndex === -1) {
    return { data: {}, body: normalized };
  }

  const block = normalized.slice(4, endIndex).trim();
  const data = {};
  for (const line of block.split('\n')) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;

    const splitAt = trimmed.indexOf(':');
    if (splitAt === -1) continue;

    const key = trimmed.slice(0, splitAt).trim();
    const value = trimmed
      .slice(splitAt + 1)
      .trim()
      .replace(/^['"]/, '')
      .replace(/['"]$/, '');

    data[key] = value;
  }

  return { data, body: normalized.slice(endIndex + 5) };
}

function routeFromRelative(relativePath) {
  let noExt = relativePath.replace(/\.mdx$/i, '');
  if (noExt.endsWith('-QC')) noExt = noExt.slice(0, -3);
  if (noExt.endsWith('/index')) noExt = noExt.slice(0, -'/index'.length);
  return noExt === 'index' ? '/' : `/${noExt}`;
}

function routeKey(routePath) {
  return routePath === '/' ? 'index' : routePath.replace(/^\//, '');
}

function sectionKey(routePath) {
  if (routePath === '/') return 'root';
  return routePath.replace(/^\//, '').split('/')[0] || 'root';
}

function resourceKey(routePath) {
  if (routePath === '/') return 'index';
  return routePath.replace(/^\//, '').replace(/\//g, '__');
}

function formatRouteLabel(routePath) {
  if (routePath === '/') return 'Home';
  const slug = routePath.replace(/^\//, '').split('/').pop() || routePath;
  return slug
    .split('-')
    .filter(Boolean)
    .map((part) => part[0]?.toUpperCase() + part.slice(1))
    .join(' ');
}

async function walkMdxFiles(dir) {
  const entries = await fs.readdir(dir, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...(await walkMdxFiles(fullPath)));
      continue;
    }

    if (entry.isFile() && fullPath.endsWith('.mdx')) {
      files.push(fullPath);
    }
  }

  return files;
}

async function ensureCleanOutput() {
  await fs.mkdir(outDir, { recursive: true });
  const existing = await fs.readdir(outDir, { withFileTypes: true });
  for (const entry of existing) {
    if (entry.isFile() && entry.name.endsWith('.json')) {
      await fs.unlink(path.join(outDir, entry.name));
    }
  }
}

async function main() {
  await ensureCleanOutput();

  const allMdx = await walkMdxFiles(docsDir);
  const enFiles = allMdx.filter((filePath) => !filePath.endsWith('-QC.mdx'));

  const manifestEntries = [];

  for (const enPath of enFiles) {
    const relative = path.relative(docsDir, enPath).replace(/\\/g, '/');
    const qcPath = enPath.replace(/\.mdx$/i, '-QC.mdx');

    const enRaw = await fs.readFile(enPath, 'utf8');
    const enParsed = parseFrontmatter(enRaw);

    let qcParsed = null;
    try {
      const qcRaw = await fs.readFile(qcPath, 'utf8');
      qcParsed = parseFrontmatter(qcRaw);
    } catch {
      qcParsed = null;
    }

    const routePath = routeFromRelative(relative);
    const fileKey = resourceKey(routePath);

    const resource = {
      EN: {
        title: String(enParsed.data.title || formatRouteLabel(routePath)),
        description: String(enParsed.data.description || ''),
        body: enParsed.body,
      },
      QC: qcParsed
        ? {
            title: String(qcParsed.data.title || enParsed.data.title || formatRouteLabel(routePath)),
            description: String(qcParsed.data.description || enParsed.data.description || ''),
            body: qcParsed.body,
          }
        : null,
    };

    await fs.writeFile(path.join(outDir, `${fileKey}.json`), `${JSON.stringify(resource, null, 2)}\n`, 'utf8');

    manifestEntries.push({
      routePath,
      routeKey: routeKey(routePath),
      sectionKey: sectionKey(routePath),
      resourceKey: fileKey,
    });
  }

  manifestEntries.sort((a, b) => a.routePath.localeCompare(b.routePath));

  const manifestSource = `// Generated by scripts/generate-doc-page-resources.mjs\n// Do not edit manually.\n\nexport interface PageResourceEntry {\n  routePath: string\n  routeKey: string\n  sectionKey: string\n  resourceKey: string\n}\n\nexport const PAGE_RESOURCE_ENTRIES: PageResourceEntry[] = ${JSON.stringify(manifestEntries, null, 2)}\n`;

  await fs.writeFile(manifestPath, manifestSource, 'utf8');

  console.log(`Generated ${manifestEntries.length} page resource files.`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
